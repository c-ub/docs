<h3 id="甘特图" tabindex="-1"><a class="header-anchor" href="#甘特图" aria-hidden="true">#</a> 甘特图</h3>
<p>甘特图(Gantt chart) 是一种项目管理工具，用来表示项目的进度和资源分配。它是一种图形化的工具，通常由水平的时间轴和垂直的任务条组成。</p>
<p>在甘特图上，每个任务都对应一个水平的条形图，表示任务的开始时间和结束时间。通过看甘特图，可以很容易地了解项目的进度、任务之间的依赖关系和资源分配情况。</p>
<p>甘特图可以帮助项目组织者更好地掌握项目进度，调整项目计划，更好地控制项目进度和资源分配。同时也可以让项目的其他成员更好的了解项目的进展情况。</p>
