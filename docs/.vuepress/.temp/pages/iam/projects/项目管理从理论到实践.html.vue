<p><strong>一些名词：</strong></p>
<ul>
<li>自测[[case]] (Self-Testing Case) 指的是在软件开发过程中，由开发人员自己制定和执行的测试用例。
<ul>
<li>自测case 是一种非常重要的质量保证手段，它可以帮助开发人员更早期地发现和修复软件中的错误和缺陷。自测case 通常包括了功能测试、性能测试、可靠性测试和兼容性测试等。</li>
<li>自测case 可以帮助开发人员确保软件的质量和可靠性，提高软件的开发效率和降低软件的维护成本。同时也可以帮助确保软件在发布前满足用户需求。</li>
</ul>
</li>
<li>[[QA]] 指质量保证 (Quality Assurance)，它是一种系统性的、预防性的过程，旨在确保产品或服务达到质量标准。
<ul>
<li>QA 的目的是通过规范、程序、标准和测试等手段来确保产品或服务达到预期的质量水平。QA 的过程包括了质量策划、质量控制、质量保证和质量改进等。</li>
<li>QA 的过程可以帮助企业或组织提高产品和服务的质量，满足客户需求，提高市场竞争力和品牌形象。QA 也可以帮助企业或组织降低不良产品的生产和服务的成本，提高生产效率和客户满意度。</li>
</ul>
</li>
